<script>
import moment from "moment/moment.js";
import {HTTP} from "@/http-common.js";

export default {
    name: "ContestRanking",
    data: () => {
        return {
            participation: {},
            contest: {},
            finished_at: '',
            total_problems: 0,
            participationList: [],
            currentPage: 1,
            links: []
        }
    },
    mounted() {
        this.onload();
    },
    methods: {
        onload() {
            let _this = this;
            HTTP.get('api/contest/result?contest_id=' + this.$route.params.id)
                .then((response) => {
                    console.log(response.data)
                    _this.participation = response.data.participation;
                    if (_this.participation) {
                        _this.contest = response.data.participation.contest;
                        _this.finished_at = moment(response.data.finished_at).format('DD-MM-yyyy hh:mm:ss')
                        _this.total_problems = response.data.participation.problems.length;
                    }
                })
                .catch((err) => {
                });
            this.getParticipation(1);
        },
        getParticipation(index) {
            let _this = this;
            _this.participationList = [];
            _this.currentPage = Number(index);
            console.log(_this.currentPage);
            HTTP.get('api/contest/ranking?contest_id=' + this.$route.params.id + '&page=' + index)
                .then((response) => {
                    _this.participationList = response.data.data;
                    _this.links = response.data.links;
                })
                .catch((err) => {
                });
        },
        format(d, h, m, s) {
            let cd = String(d), ch = String(h), cm = String(m), cs = String(s);
            if (cd.length == 1) {
                cd = '0' + cd;
            }
            if (ch.length == 1) {
                ch = '0' + ch;
            }
            if (cm.length == 1) {
                cm = '0' + cm;
            }
            if (cs.length == 1) {
                cs = '0' + cs;
            }
            return cd + 'd' + ' ' + ch + 'h' + ' ' + cm + 'm' + ' ' + cs + 's';
        }
    }
}
</script>

